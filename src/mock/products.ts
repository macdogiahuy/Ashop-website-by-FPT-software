function cloneProducts(products: Product[], loop: number) {
  const clonedProducts: Product[] = [...products];
  let id = 5;
  for (let i = 1; i <= loop; i++) {
    const temp: Product[] = products.map((product) => ({
      ...product,
      id: `${++id}`,
      name: `${product.name} ${i}`,
      price: product.price + i,
      sold: product.sold + i,
    }));
    clonedProducts.push(...temp);
  }
  return clonedProducts;
}

